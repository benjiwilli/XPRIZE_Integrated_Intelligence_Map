export function generateAIDrafts(entity: EcosystemEntity, actionType: "summary" | "missing" | "followup" | "briefing") {
  const result: { title: string; content: string } = { title: "", content: "" };
  
  if (actionType === "summary") {
    result.title = `Challenge Fit Assessment: ${entity.name}`;
    result.content = `Critical Analysis of technological positioning:
- **Challenge Match**: High alignment with ${entity.challengeAreas.join(" & ")}.
- **Readiness Rating**: Decided at "${entity.readinessStage.toUpperCase()}" level, with ${entity.approvedEvidenceCount}/${entity.evidenceCount} verified criteria sheets.
- **Strategic Recommendation**: Candidate has potential to compete or serve as a foundational anchor node. Recommend tracking next milestone action: "${entity.nextAction || 'Milestone verification'}".`;
  } else if (actionType === "missing") {
    result.title = `Missing Evidence Audit: ${entity.name}`;
    result.content = `Our ecosystem mapping system identified the following validation gaps matching the ${entity.challengeAreas[0]} rubric parameters:
1. Third-party testing certification files (Missing for ${entity.readinessStage} validation criteria).
2. Local sub-component telemetry logs or pilot facility operational blueprints.
3. Verified proof of native Canadian intellectual property development and commercialization rights.
4. Human-reviewed stakeholder safety surveys (Currently: Unverified).`;
  } else if (actionType === "followup") {
    result.title = `Draft Outreach Pathway to ${entity.name}`;
    result.content = `Subject: Milestone Review: ${entity.challengeAreas[0]} Portfolio Collaboration

Dear ${entity.name} Team,

I am writing on behalf of the Demo Engagement Lead. In coordinating our national mapping intelligence for the 2026 Grand Challenge series, we reviewed your promising work in ${entity.city} regarding high-readiness ${entity.challengeAreas.join("/")} advances.

To assist in recommending your validation pipeline for draft briefings and possible partnership pathways, we would love to review your latest telemetry charts. Do you have 15 minutes next Wednesday for a dedicated milestone touchpoint?

Best regards,
${entity.relationshipOwner}
Canada Hub — Demo Engagement Team
(Calgary Anchor office)`;
  } else {
    result.title = `Briefing Entry Template for ${entity.name}`;
    result.content = `BRIEFING EXCERPT [DEMO ACCESS]
-----------------------------------------
Entity: ${entity.name} (${entity.city}, ${entity.province})
Taxonomy Fit: ${entity.challengeAreas.join(", ")}
Owner Lead: ${entity.relationshipOwner}
Validation Weight: Priority Score ${entity.priorityScore} (Stage: ${entity.readinessStage.toUpperCase()})

EXECUTIVE CASE:
Developing highly aligned ${entity.challengeAreas[0]} systems. This entity has successfully reported active validations and pilots near ${entity.city}. Recommend adding this node to the regional innovation cluster briefing stack right away.`;
  }

  return result;
}
